package com.yj.board.dto;

public class FurnitureDto {
	
	int fnt_id;
	String fnt_name;
	String kinds;
	String colors;
	String site;
	String heights;
	String horizontal;
	String vertical;
	String file3d;
	
	public int getFnt_id() {
		return fnt_id;
	}
	public void setFnt_id(int fnt_id) {
		this.fnt_id = fnt_id;
	}
	
	public String getFnt_name() {
		return fnt_name;
	}
	public void setFnt_name(String fnt_name) {
		this.fnt_name = fnt_name;
	}
	
	public String getKinds() {
		return kinds;
	}
	public void setKinds(String kinds) {
		this.kinds = kinds;
	}
	
	public String getColors() {
		return colors;
	}
	public void setColors(String colors) {
		this.colors = colors;
	}
	
	public String getSite() {
		return site;
	}
	public void setSite(String site) {
		this.site = site;
	}
	
	public String getHeights() {
		return heights;
	}
	public void setHeights(String heights) {
		this.heights = heights;
	}
	
	public String getHorizontal() {
		return horizontal;
	}
	public void setHorizontal(String horizontal) {
		this.horizontal = horizontal;
	}
	
	public String getVertical() {
		return vertical;
	}
	public void setVertical(String vertical) {
		this.vertical = vertical;
	}
	
	public String getFile3d() {
		return file3d;
	}
	public void setFile3d(String file3d) {
		this.file3d = file3d;
	}
	 
}
